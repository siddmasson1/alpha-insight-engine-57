// Edge function: quant-analyst-chat
// Conversational AI analyst for follow-up questions on portfolio impact analysis
import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req: Request) => {
  if (req.method === "OPTIONS") {
    return new Response("ok", { headers: corsHeaders });
  }

  try {
    const { messages, context } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const systemPrompt = `You are a senior quantitative analyst at a top hedge fund. You have deep expertise in risk management, portfolio construction, and market microstructure.

You are having a conversation with a portfolio manager about a specific news event and its impact on their portfolio.

CONTEXT:
- News Event: "${context.headline}" (Source: ${context.source})
- Impact Analysis Summary: ${context.analysis?.summary || "N/A"}
- Impact Score: ${context.analysis?.impactScore || "N/A"}/10
- Portfolio Impact: ${context.analysis?.overallPortfolioImpactPct?.toFixed(2) || "N/A"}%

PORTFOLIO POSITIONS:
${JSON.stringify(context.portfolio, null, 2)}

AFFECTED POSITIONS:
${JSON.stringify(context.analysis?.portfolioImpact || [], null, 2)}

HEDGE RECOMMENDATIONS:
${JSON.stringify(context.analysis?.hedgeRecommendations || [], null, 2)}

Guidelines:
- Be specific with numbers and tickers
- Reference the actual portfolio positions when relevant
- Provide actionable insights
- Use markdown formatting for readability
- Keep responses concise but thorough (2-4 paragraphs max)
- When discussing historical precedents, cite specific dates and events
- Consider risk factors like rates sensitivity, equity beta, sector exposure`;

    const aiMessages = [
      { role: "system", content: systemPrompt },
      ...messages.map((m: { role: string; content: string }) => ({
        role: m.role,
        content: m.content,
      })),
    ];

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: aiMessages,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again shortly." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI chat failed" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    const aiData = await response.json();
    const reply = aiData.choices?.[0]?.message?.content || "I couldn't generate a response.";

    return new Response(JSON.stringify({ reply }), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  } catch (e) {
    console.error("quant-analyst-chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
